pub fn sample_stats(count: Vec<i32>) -> Vec<f64> {
    let mut ret = Vec::new();
    ret.push(count.iter().position(|&x| x > 0).unwrap_or(0) as f64); //min
    ret.push(count.iter().rposition(|&x| x > 0).unwrap_or(0) as f64); //max
    let mean_ratio = count.iter().enumerate().fold((0_f64, 0_u64), |acc, p| {
        (acc.0 + (p.0 as f64 * *p.1 as f64), acc.1 + *p.1 as u64)
    });
    ret.push(mean_ratio.0 / mean_ratio.1 as f64); //mean
    let total = (mean_ratio.1 + 1) / 2;
    let mut temp = total as i32;
    let left = count
        .iter()
        .position(|&x| {
            temp -= x;
            temp <= 0
        })
        .unwrap_or(count.len() - 1);
    temp = total as i32;
    let right = count
        .iter()
        .rposition(|&x| {
            temp -= x;
            temp <= 0
        })
        .unwrap_or(0);
    ret.push((left + right) as f64 / 2_f64);
    ret.push(count.iter().enumerate().max_by_key(|&p| p.1).unwrap().0 as f64); //mode
    ret
}

